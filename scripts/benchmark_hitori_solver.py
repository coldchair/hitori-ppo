#!/usr/bin/env python3
"""
Benchmark [philiplugt/hitori-solver](https://github.com/philiplugt/hitori-solver) on puzzles
generated by ``hitori_env`` (same distribution as training).

Run from repo root::

    python scripts/benchmark_hitori_solver.py --size 8 --count 20 --seed 0

Prepends ``hitori-solver/source`` to ``sys.path`` and calls the smart CSP **without** the
upstream ``@timeit`` wrapper (that wrapper never returns ``(counter, solution)``).
"""

from __future__ import annotations

import argparse
import importlib.util
import statistics
import sys
import time
from pathlib import Path
from typing import Any

REPO = Path(__file__).resolve().parents[1]
SOLVER_SRC = REPO / "hitori-solver" / "source"
GENERATOR_PATH = REPO / "hitori_env" / "envs" / "hitori_generator.py"


def _load_generator():
    """Load ``generate_random_hitori_game`` without importing ``hitori_env`` (avoids gymnasium)."""

    spec = importlib.util.spec_from_file_location("hitori_generator_standalone", GENERATOR_PATH)
    if spec is None or spec.loader is None:
        raise RuntimeError(f"cannot load spec for {GENERATOR_PATH}")
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod.generate_random_hitori_game


def _solve_smart_core(puzzle: list[list[int]]) -> tuple[int, list[list[Any]] | None]:
    """Same logic as ``hitori-solver/source/hitori.py::solve_smart`` (undecorated)."""

    from hitori_rules import (  # noqa: PLC0415
        black_allowed,
        cell_surrounded,
        check_solution,
        copy as hitori_copy_state,
        fc_black,
        fc_white,
        load_puzzle,
        test_white_connected,
    )

    counter = 0
    nodes = [[0, 0]]
    states = [load_puzzle(puzzle)]

    while nodes:
        i, j = nodes.pop()
        state = states.pop()

        if check_solution(state):
            return counter, state["puzzle"]

        if i >= len(state["puzzle"]):
            continue

        state = cell_surrounded(state, i, j)
        if not state:
            continue

        for d in state["domain"][i][j]:
            if d == "V":
                states.append(state)
                nodes.append([i, j + 1] if j + 1 < len(state["puzzle"][i]) else [i + 1, 0])
                continue

            if d == "W":
                white_state = hitori_copy_state(state)
                white_state["domain"][i][j] = "W"
                counter += 1
                new_domain_white = fc_white(white_state, i, j)
                if not new_domain_white:
                    continue
                states.append(white_state)
                nodes.append([i, j + 1] if j + 1 < len(state["puzzle"][i]) else [i + 1, 0])

            if d == "B" and black_allowed(state, i, j):
                black_state = hitori_copy_state(state)
                black_state["puzzle"][i][j] = "B"
                black_state["domain"][i][j] = "B"
                counter += 1
                if not test_white_connected(black_state["domain"], black_state["edges"]):
                    continue
                black_state = fc_black(black_state, i, j)
                if not black_state:
                    continue
                states.append(black_state)
                nodes.append([i, j + 1] if j + 1 < len(state["puzzle"][i]) else [i + 1, 0])
    return counter, None


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--size", type=int, default=8, help="Board side length n (n×n).")
    p.add_argument("--count", type=int, default=10, help="Number of random puzzles.")
    p.add_argument("--seed", type=int, default=0, help="RNG seed for generator.")
    args = p.parse_args()

    if not SOLVER_SRC.is_dir():
        raise SystemExit(f"Solver source not found: {SOLVER_SRC} (clone hitori-solver under repo root)")

    sys.path.insert(0, str(SOLVER_SRC))
    sys.path.insert(0, str(REPO))

    generate_random_hitori_game = _load_generator()

    import numpy as np  # noqa: E402

    rng = np.random.default_rng(args.seed)
    times: list[float] = []
    solved = 0
    nodes_list: list[int] = []

    print(
        f"repo={REPO}\n"
        f"generator={GENERATOR_PATH.name} (loaded without gymnasium)\n"
        f"solver=hitori-solver/source (smart CSP)\n"
        f"size={args.size} count={args.count} seed={args.seed}\n",
        flush=True,
    )

    for i in range(args.count):
        seed_i = int(rng.integers(0, 2**31 - 1))
        grid = generate_random_hitori_game(args.size, seed=seed_i)
        puzzle = grid.astype(int).tolist()

        t0 = time.perf_counter()
        counter, solution = _solve_smart_core(puzzle)
        elapsed = time.perf_counter() - t0
        times.append(elapsed)
        nodes_list.append(int(counter))
        ok = solution is not None
        if ok:
            solved += 1
        status = "ok" if ok else "no_solution"
        print(f"  [{i+1:3d}/{args.count}] seed={seed_i} time={elapsed:.4f}s nodes={counter} {status}", flush=True)

    print("", flush=True)
    print(
        f"Summary: solved {solved}/{args.count}\n"
        f"  time_s: mean={statistics.mean(times):.4f}  median={statistics.median(times):.4f} "
        f"min={min(times):.4f}  max={max(times):.4f}\n"
        f"  search_nodes: mean={statistics.mean(nodes_list):.1f}  median={statistics.median(nodes_list):.1f}",
        flush=True,
    )


if __name__ == "__main__":
    main()
